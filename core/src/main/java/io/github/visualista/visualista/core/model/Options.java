package io.github.visualista.visualista.core.model;

public class Options {

	private String NOVEL_FILE_PATH = null;
	private String SCENE_FILE_PATH = null;
	private String ACTOR_FILE_PATH = null;
	private String ACTION_FILE_PATH = null;
	
	public String getNOVEL_FILE_PATH() {
		return NOVEL_FILE_PATH;
	}
	public void setNOVEL_FILE_PATH(String nOVEL_FILE_PATH) {
		NOVEL_FILE_PATH = nOVEL_FILE_PATH;
	}
	public String getSCENE_FILE_PATH() {
		return SCENE_FILE_PATH;
	}
	public void setSCENE_FILE_PATH(String sCENE_FILE_PATH) {
		SCENE_FILE_PATH = sCENE_FILE_PATH;
	}
	public String getACTOR_FILE_PATH() {
		return ACTOR_FILE_PATH;
	}
	public void setACTOR_FILE_PATH(String aCTOR_FILE_PATH) {
		ACTOR_FILE_PATH = aCTOR_FILE_PATH;
	}
	public String getACTION_FILE_PATH() {
		return ACTION_FILE_PATH;
	}
	public void setACTION_FILE_PATH(String aCTION_FILE_PATH) {
		ACTION_FILE_PATH = aCTION_FILE_PATH;
	}
}
